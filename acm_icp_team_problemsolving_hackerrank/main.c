int* acmTeam(int topic_count, char** topic, int* result_count) {
    size_t topicNum = strlen(topic[0]);
    int count = 0;
    int max = 0;
    int result = 0;
    int itr, i, j;
    for(i=0;i<topic_count-1;i++)
    {
        for(j=i+1;j<topic_count;j++)
        {
            for(itr =0;itr<topicNum;itr++)
            {
                if(topic[i][itr] == '1' || topic[j][itr]== '1')
                {
                    result++;
                }
            }
            if(result>max)
            {
                max = result;
                count = 1;
            }
            else if(result==max)
            {
                count ++;
            }
            result = 0;
        }
    }
    *result_count = 2;
    int *resultArr =  malloc(2 * sizeof(int));
    resultArr[0] = max;
    resultArr[1] = count;
    return resultArr;
}

